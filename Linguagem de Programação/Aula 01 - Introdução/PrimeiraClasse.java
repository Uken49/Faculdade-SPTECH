// Os comandos tem abreviações/atalhos para facilitar a programação.
// Os atalhos tem como nome a primeira letra de cada palavra reservada.

// Uma classe em Java tem como padrão o nome em "PascalCase" e sempre no singular, entretanto
// durante as aulas, poderei estar colocando o nome no plural para fins didático.
public class PrimeiraClasse {

    // Atalho - psvm ou main
    // O método "main" é o ponto de partida, é a classe que é executa ao iniciar a aplicação
    public static void main(String[] args) {

        // Atalho - sout
        // Para exibir informações no console utilizaremos o método "println" da Classe System
        System.out.println("Olá mundo");
    }
}
